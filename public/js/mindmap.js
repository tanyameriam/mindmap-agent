/**
 * MindMap module — handles rendering with markmap and PDF export
 */
const MindMap = (() => {
  let currentMarkmapInstance = null;
  let currentSvgElement = null;
  let currentMarkdown = null;

  function render(markdown) {
    currentMarkdown = markdown;
    const container = document.getElementById('mindmapContainer');

    // Clear placeholder
    container.innerHTML = '';

    // Create SVG element
    const svg = document.createElementNS('http://www.w3.org/2000/svg', 'svg');
    svg.setAttribute('id', 'mindmapSvg');
    svg.style.width = '100%';
    svg.style.height = '100%';
    container.appendChild(svg);
    currentSvgElement = svg;

    // Use markmap to transform and render
    try {
      const transformer = new markmap.Transformer();
      const { root } = transformer.transform(markdown);

      currentMarkmapInstance = markmap.Markmap.create(svg, {
        colorFreezeLevel: 2,
        duration: 500,
        maxWidth: 300,
        zoom: true,
        pan: true,
        autoFit: true,
        paddingX: 20,
      }, root);

      // Show download actions
      document.getElementById('mindmapActions').style.display = 'flex';

      // Style the SVG with dark theme colors
      setTimeout(() => {
        styleDarkTheme(svg);
      }, 600);

    } catch (err) {
      console.error('Markmap render error:', err);
      container.innerHTML = `
        <div class="mindmap-placeholder">
          <div class="placeholder-icon">⚠️</div>
          <p>Error rendering mind map</p>
          <p class="placeholder-sub">${err.message}</p>
        </div>
      `;
    }
  }

  function styleDarkTheme(svg) {
    // Style text nodes for dark theme
    svg.querySelectorAll('text').forEach(text => {
      text.style.fill = '#e8e8f0';
      text.style.fontFamily = "'Inter', sans-serif";
      text.style.fontSize = '14px';
    });

    // Style circles
    svg.querySelectorAll('circle').forEach(circle => {
      const r = parseFloat(circle.getAttribute('r'));
      if (r < 4) {
        circle.style.stroke = '#8b5cf6';
      }
    });
  }

  async function downloadPDF() {
    if (!currentSvgElement) return;

    const container = document.getElementById('mindmapContainer');
    const btn = document.getElementById('downloadPdfBtn');
    const originalText = btn.querySelector('span').textContent;

    btn.querySelector('span').textContent = 'Generating...';
    btn.disabled = true;

    try {
      // Use html2canvas to capture the SVG container
      const canvas = await html2canvas(container, {
        backgroundColor: '#08080f',
        scale: 2,
        useCORS: true,
        logging: false,
        width: container.offsetWidth,
        height: container.offsetHeight
      });

      const { jsPDF } = window.jspdf;
      const imgData = canvas.toDataURL('image/png');

      // Determine orientation based on aspect ratio
      const aspectRatio = canvas.width / canvas.height;
      const orientation = aspectRatio > 1 ? 'landscape' : 'portrait';

      const pdf = new jsPDF({
        orientation,
        unit: 'mm',
        format: 'a4'
      });

      const pageWidth = pdf.internal.pageSize.getWidth();
      const pageHeight = pdf.internal.pageSize.getHeight();

      // Scale image to fit page with padding
      const padding = 10;
      const maxWidth = pageWidth - 2 * padding;
      const maxHeight = pageHeight - 2 * padding;

      let imgWidth = maxWidth;
      let imgHeight = (canvas.height / canvas.width) * imgWidth;

      if (imgHeight > maxHeight) {
        imgHeight = maxHeight;
        imgWidth = (canvas.width / canvas.height) * imgHeight;
      }

      const x = (pageWidth - imgWidth) / 2;
      const y = (pageHeight - imgHeight) / 2;

      pdf.addImage(imgData, 'PNG', x, y, imgWidth, imgHeight);

      // Add title
      pdf.setFontSize(8);
      pdf.setTextColor(150, 150, 150);
      pdf.text('Generated by MindMap Agent', padding, pageHeight - 5);

      pdf.save('mindmap.pdf');

    } catch (err) {
      console.error('PDF export error:', err);
      alert('Failed to generate PDF. Please try again.');
    } finally {
      btn.querySelector('span').textContent = originalText;
      btn.disabled = false;
    }
  }

  function reset() {
    const container = document.getElementById('mindmapContainer');
    container.innerHTML = `
      <div class="mindmap-placeholder">
        <div class="placeholder-icon">🧠</div>
        <p>Your mind map will appear here</p>
        <p class="placeholder-sub">Answer the agent's questions to generate it</p>
      </div>
    `;
    document.getElementById('mindmapActions').style.display = 'none';
    currentMarkmapInstance = null;
    currentSvgElement = null;
    currentMarkdown = null;
  }

  return { render, downloadPDF, reset };
})();
